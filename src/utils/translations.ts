function normalizeKey(value: string): string {
  return value
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function titleCase(value: string): string {
  return value
    .replace(/\s+/g, ' ')
    .trim()
    .replace(/(^|\s)(\S)/g, (match) => match.toUpperCase());
}

function translateTerm(
  value: string | undefined | null,
  dictionary: Record<string, string>,
): string {
  if (!value) return '';
  return dictionary[value] ?? dictionary[normalizeKey(value)] ?? value;
}

export function translateBodyPart(value: string | undefined | null): string {
  return translateTerm(value, BODY_PARTS);
}

export function translateEquipment(value: string | undefined | null): string {
  return translateTerm(value, EQUIPMENT);
}

export function translateMuscle(value: string | undefined | null): string {
  return translateTerm(value, MUSCLES);
}

export function translateSearchTerm(value: string): string {
  const normalized = normalizeKey(value);
  if (!normalized) return '';
  return SEARCH_TERMS[normalized] ?? value.trim();
}

export function translateExerciseName(name: string): string {
  const normalized = normalizeKey(name);
  const exact = EXACT_EXERCISE_NAMES[normalized];
  if (exact) return exact;

  const translated = WORD_REPLACEMENTS.reduce(
    (current, [pattern, replacement]) => current.replace(pattern, replacement),
    normalized,
  );

  return translated === normalized ? name : titleCase(translated);
}

export function translateExerciseNames(
  name: string,
  bodyPart?: string,
  target?: string,
  equipment?: string,
): {
  name: string;
  bodyPart: string;
  target: string;
  equipment: string;
} {
  return {
    name: translateExerciseName(name),
    bodyPart: translateBodyPart(bodyPart),
    target: translateMuscle(target),
    equipment: translateEquipment(equipment),
  };
}
